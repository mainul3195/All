#include<bits/stdc++.h>
#define pi (2*acos(0.0))
#define Swap(x,y) (x)^=(y)^=(x)^=(y)
#define all(x) x.begin(), x.end()
using namespace std;
int main()
{
    ios_base::sync_with_stdio(0); cin.tie(0);

    int t;
    cin >> t;
    while(t--)
    {
        int n;
        cin >> n;
        vector<long long> v(n);
        for(auto &i: v)
            cin >> i;
        long long ans = 0;
        for(int i=1; i<n; i++)
            ans = max(ans, v[i]*v[i-1]);
        cout << ans << "\n";
    }

    return 0;
}
